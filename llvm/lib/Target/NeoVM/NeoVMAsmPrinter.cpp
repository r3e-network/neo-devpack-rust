bool NeoVMAsmPrinter::runOnMachineFunction(MachineFunction &MF) {
  writeScript(MF);
  writeManifest(MF);
  return AsmPrinter::runOnMachineFunction(MF);
}

void NeoVMAsmPrinter::writeScript(const MachineFunction &MF) {
  // Write script bytecode to NEF container
  if (!NefContainer) {
    NefContainer = std::make_unique<NEFContainer>();
  }
  
  // Collect bytecode from machine function
  SmallVector<uint8_t, 1024> bytecode;
  
  for (const MachineBasicBlock &MBB : MF) {
    for (const MachineInstr &MI : MBB) {
      if (MI.isPseudo())
        continue;

      if (auto Encoding = NeoVMEncoding::getOpcodeEncoding(MI.getOpcode())) {
        bytecode.push_back(Encoding->Byte);
        uint64_t ImmValue = 0;
        bool HasImm = false;
        for (const MachineOperand &MO : MI.operands()) {
          if (!MO.isImm())
            continue;
          ImmValue = static_cast<uint64_t>(MO.getImm());
          HasImm = true;
          break;
        }

        if (Encoding->HasSizePrefix)
          for (unsigned I = 0; I < Encoding->OperandPrefixSize; ++I)
            bytecode.push_back(static_cast<uint8_t>((ImmValue >> (8 * I)) & 0xFF));

        if (Encoding->HasImmediate) {
          if (!HasImm && !Encoding->HasSizePrefix)
            ImmValue = 0;
          for (unsigned I = 0; I < Encoding->OperandSize; ++I)
            bytecode.push_back(static_cast<uint8_t>((ImmValue >> (8 * I)) & 0xFF));
        }
      }
    }
  }
  
  // Set script in NEF container
  NefContainer->script.assign(bytecode.begin(), bytecode.end());
}

void NeoVMAsmPrinter::writeManifest(const MachineFunction &MF) {
  if (!NefContainer) {
    NefContainer = std::make_unique<NEFContainer>();
  }
  
  // Generate manifest for the function
  NEFManifestGenerator manifestGen;
  manifestGen.contractName = MF.getName().str();
  manifestGen.version = "1.0.0";
  manifestGen.author = "neo-llvm";
  manifestGen.description = "Contract generated by NeoVM LLVM backend";
  
  // Add function as a method
  manifestGen.addMethod(MF.getName(), {}, "Void");
  
  // Set manifest in NEF container
  NefContainer->manifest = manifestGen.generate();
}
