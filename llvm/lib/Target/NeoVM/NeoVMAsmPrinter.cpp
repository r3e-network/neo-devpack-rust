NeoVMAsmPrinter::NeoVMAsmPrinter(TargetMachine &TM,
                                 std::unique_ptr<MCStreamer> Streamer)
    : AsmPrinter(TM, std::move(Streamer)) {}

NeoVMAsmPrinter::~NeoVMAsmPrinter() = default;

StringRef NeoVMAsmPrinter::getPassName() const {
  return "NeoVM Assembly Printer";
}

void NeoVMAsmPrinter::emitInstruction(const MachineInstr *MI) {
  if (!MI)
    return;

  auto Encoding = NeoVMEncoding::getOpcodeEncoding(MI->getOpcode());
  if (!Encoding) {
    LLVM_DEBUG(dbgs() << "[NeoVMAsmPrinter] skipping opcode " << MI->getOpcode()
                      << '\n');
    return;
  }

  SmallVector<uint8_t, 16> Buffer;
  Buffer.push_back(Encoding->Byte);

  auto AppendLE = [&Buffer](uint64_t Value, unsigned Bytes) {
    for (unsigned I = 0; I < Bytes; ++I)
      Buffer.push_back(static_cast<uint8_t>((Value >> (8 * I)) & 0xFF));
  };

  uint64_t Immediate = 0;
  bool HasImm = false;
  for (const MachineOperand &MO : MI->operands()) {
    if (!MO.isImm())
      continue;
    Immediate = static_cast<uint64_t>(MO.getImm());
    HasImm = true;
    break;
  }

  if (Encoding->HasSizePrefix)
    AppendLE(Immediate, Encoding->OperandPrefixSize);

  if (Encoding->HasImmediate) {
    if (!HasImm && !Encoding->HasSizePrefix)
      LLVM_DEBUG(dbgs() << "[NeoVMAsmPrinter] opcode " << MI->getOpcode()
                        << " lacks immediate, emitting zero\n");
    AppendLE(Immediate, Encoding->OperandSize);
  }

  OutStreamer->emitBytes(StringRef(reinterpret_cast<const char *>(Buffer.data()),
                                   Buffer.size()));
}

